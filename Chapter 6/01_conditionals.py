a = int(input("Enter a number: "))

# if statement
if a > 0:
    print("The number is positive.")

#if-else statement
if (a<0):
    print("The number is negative.")
else:
    print("The number is positive.")    

# if-elif-else statement
if (a<0):
    print("The number is negative.")
elif(a==0):
    print("The number is zero.")    
else:
    print("The number is positive.")   